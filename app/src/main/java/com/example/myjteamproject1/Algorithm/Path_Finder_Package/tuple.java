package com.example.myjteamproject1.Algorithm.Path_Finder_Package;

public class tuple {
	private int x;
	private int y;
	private int z;
	tuple(int first, int second, int third){
		x = first;
		y = second;
		z = third;
	}
	public int get_first() {
		return x;
	}
	public int get_second() {
		return y;
	}
	public int get_third() {
		return z;
	}
}
